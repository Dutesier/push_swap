#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	int	i;

	i = 0;
	while (n > 0 && s1[i] && s2[i])
	{
		if (s1[i] != s2[i])
			return ((unsigned char)s1[i] - (unsigned char)s2[i]);
		i++;
		n--;
	}
	if (n > 0 && s2[i])
		return ((0 - (unsigned char)s2[i]));
	if (n > 0 && s1[i])
		return ((unsigned char)s1[i]);
	return (0);
}
