#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*d;

	d = (char *)s;
	while (*d != '\0')
	{
		if (*d == (char)c)
			return (d);
		d++;
	}
	if (*d == (char)c)
		return (d);
	else
		return (0);
}
