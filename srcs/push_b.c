#include "../includes/push_swap.h"

int push_b(t_stack *b, t_stack *a)
{
	if (a->top <= -1)
		return (0);
	b->top++;
	b->table[b->top] = a->table[a->top--];
	ft_putstr_fd("pb\n", 1);
	return (1);
}

